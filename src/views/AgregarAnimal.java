    public Especie getEspecie() {
        String nombreEspecie = String.valueOf(comboBoxEspecie.getSelectedItem());
        for (Especie especie : Controlador.getEspecies()) {
            if (nombreEspecie.equals(especie.getNombre())) {
                return especie;
            }
        }
        return null;
    }
    
    public Sector getSector() {
        String nombreSector = String.valueOf(comboBoxSector.getSelectedItem());
        System.out.println(""+nombreSector);
        for (Sector sector : Controlador.getSectores()) {
            if (nombreSector.equals(String.valueOf(sector.toString()))) {
                
                return sector;
            }
        }
        return null;
    }
    
    public Pais getPais() {
        String nombrePais = String.valueOf(comboBoxPais.getSelectedItem());
        for (Pais pais : Controlador.getPaises()) {
            if (nombrePais.equals(pais.getNombre())){
                return pais;
            }
        }
        return null;
    }   
    
